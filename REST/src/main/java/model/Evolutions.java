package model;

public class Evolutions {
	
	private String num;
	private String name;
	
	public Evolutions() {
		super();
	}
	
	public Evolutions(String num, String name) {
		super();
		this.num = num;
		this.name = name;
	}
	public String getNum() {
		return num;
	}
	public void setNum(String num) {
		this.num = num;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	

}
